#Taking two inputs from user

first_number=int(input("enter first number:"))
second_number=int(input("enter second number:"))

#Addition
sum=first_number+second_number
print(f"sum of {first_number} and {second_number} is ",sum)

#Substarction
sub=first_number-second_number
print(f"substraction of {first_number} and {second_number} is",sub)


#Multiplication
mul=first_number*second_number
print(f"multiplication of {first_number} and {second_number} is",mul)


#Division
division = first_number/second_number
print(f"division of {first_number} and {second_number} is", division)


#Floor division
fdivision=first_number//second_number
print(f"floor division of {first_number} and {second_number} is ",fdivision)

#Power 
power=first_number**second_number
print(f"power of {first_number} of {second_number} is ",power)